    private void btnCalculator(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculator
        // TODO add your handling code here:
        try {
            double txt1 = Double.parseDouble(jTextField1.getText());
            double txt2 = Double.parseDouble(jTextField2.getText());
            switch (evt.getActionCommand()) {
                case "+":
                    jTextField3.setText(String.valueOf(txt1 + txt2));
                    break;
                case "-":
                    jTextField3.setText(String.valueOf(txt1 - txt2));
                    break;
                case "*":
                    jTextField3.setText(String.valueOf(txt1 * txt2));
                    break;
                case "/":
                    jTextField3.setText(String.valueOf(txt1 / txt2));
                    break;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lá»—i: " + e.getMessage()); 
        }
            
    }//GEN-LAST:event_btnCalculator

